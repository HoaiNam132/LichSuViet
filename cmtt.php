        <p>Tháng 8-1945, Hội nghị đại biểu toàn quốc của Đảng họp tại Tân Trào (Tuyên Quang) khẳng định: “Cơ hội rất tốt cho ta giành độc lập đã tới” và quyết định phát động toàn dân khởi nghĩa giành chính quyền từ tay phát xít Nhật và tay sai trước khi quân Đồng minh vào Đông Dương; đề ra ba nguyên tắc bảo đảm tổng khởi nghĩa thắng lợi, đó là: Tập trung, thống nhất, kịp thời. 23 giờ ngày 13-8-1945, Ủy ban Khởi nghĩa ra Quân lệnh số 1 hiệu triệu toàn dân tổng khởi nghĩa. Ngày 16-8-1945, Đại hội Quốc dân họp tại Tân Trào thông qua “10 chính sách lớn của Việt Minh”; thông qua “Lệnh tổng khởi nghĩa”; quy định quốc kỳ, quốc ca; thành lập Ủy ban Dân tộc giải phóng Trung ương, tức Chính phủ Lâm thời do đồng chí Hồ Chí Minh làm Chủ tịch. Chủ tịch Hồ Chí Minh gửi thư kêu gọi nhân dân cả nước tổng khởi nghĩa, trong đó chỉ rõ: “Giờ quyết định cho vận mệnh dân tộc ta đã đến. Toàn quốc đồng bào hãy đứng dậy đem sức ta mà tự giải phóng cho ta”.</p>

        <p>Dưới sự lãnh đạo của Đảng và Chủ tịch Hồ Chí Minh, nhân dân cả nước đồng loạt vùng dậy, tiến hành tổng khởi nghĩa, giành chính quyền. Từ ngày 14 đến ngày 18-8, cuộc tổng khởi nghĩa nổ ra giành được thắng lợi ở nông thôn đồng bằng Bắc Bộ, đại bộ phận miền Trung, một phần miền Nam và ở các thị xã: Bắc Giang, Hải Dương, Hà Tĩnh, Hội An, Quảng Nam... Ngày 19-8, khởi nghĩa giành chính quyền thắng lợi ở Hà Nội. Ngày 23-8, khởi nghĩa thắng lợi ở Huế và ở Bắc Kạn, Hòa Bình, Hải Phòng, Hà Đông, Quảng Bình, Quảng Trị, Bình Định, Gia Lai, Bạc Liêu... Ngày 25-8, khởi nghĩa thắng lợi ở Sài Gòn - Gia Định, Kon Tum, Sóc Trăng, Vĩnh Long, Trà Vinh, Biên Hòa, Tây Ninh, Bến Tre... Ở Côn Đảo, Đảng bộ nhà tù Côn Đảo đã lãnh đạo các chiến sĩ cách mạng bị giam cầm nổi dậy giành chính quyền.</p>
