        <h2>Theo dấu chân Người</h2>

            <p>- Ngày 19-8-1922, Báo “L’Humanité” (Nhân đạo) đăng bài “Chủ nghĩa cộng sản và thanh niên Trung Quốc” của Nguyễn Ái Quốc giới thiệu tiến trình và những mốc phát triển của phong trào thanh niên ở Trung Quốc nhân Đại hội Đoàn Thanh niên Cộng sản toàn Trung Quốc vừa họp thành công 3 tháng trước (5-1922).</p>

            <p>- Ngày 19-8-1947, nhân dịp kỷ niệm Cách mạng Tháng Tám, Chủ tịch Hồ Chí Minh viết “Thư gửi đồng bào toàn quốc” và “Thư gửi đồng bào Việt Bắc”. Với đồng bào cả nước, Bác phân tích: “Cách mạng Tháng Tám đã giải phóng đồng bào ta ra khỏi chế độ quân chủ chuyên chế và xiềng xích thực dân... đã xây dựng cho nhân dân ta cái nền tảng Dân chủ Cộng hòa và thống nhất độc lập. Noi gương Cách mạng 1776 của Mỹ, Cách mạng Tháng Tám tranh tự chủ chống ngoại xâm. Cũng như Cách mạng 1789 của Pháp, Cách mạng Tháng Tám thực hành lý tưởng: Bình đẳng, Tự do, Bác ái. Theo gót Cách mạng 1911 của Trung Quốc, Cách mạng Tháng Tám thực hiện chủ nghĩa: Dân tộc, dân quyền, dân sinh”.</p>
            <img src="img/cmtt4.jpg">
            <p>Còn trong thư gửi đồng bào của căn cứ địa cách mạng, Bác viết: “Đồng bào Việt Bắc đã có một lịch sử cách mạng rất vẻ vang. Xưa kia cụ Hoàng Hoa Thám và những vị anh hùng khác đã dựng cờ khởi nghĩa chống Pháp suốt mấy mươi năm. Gần đây Việt Bắc là căn cứ địa oai hùng của Quân giải phóng để chống Nhật, kháng Pháp… Có sự vẻ vang đó là vì toàn thể đồng bào Việt Bắc: Kinh, Thổ, Mán, Nùng, Mèo, v.v... ai cũng yêu nước, ai cũng không chịu làm nô lệ, ai cũng đoàn kết, ai cũng hăng hái ủng hộ cách mạng”.</p>

            <p>Cũng trong thời gian này, Bác Hồ còn viết “Thư gửi Nhi đồng toàn quốc” căn dặn: “… Việc gì có ích cho kháng chiến, có ích cho Tổ quốc thì các cháu nên gắng sức làm. Làm được bao nhiêu tốt bấy nhiêu. Tuổi các cháu còn nhỏ, thì các cháu làm những công việc nhỏ. Nhiều công việc nhỏ cộng lại thành công việc to. Bác mong các cháu làm việc và học hành, cho xứng đáng là nhi đồng của nước Việt Nam Dân chủ Cộng hòa, thống nhất và độc lập”.</p>
