            <p>Ngày 06/12/1953, Bộ Chính trị đã họp và ra quyết định mở Chiến dịch Điện Biên Phủ và thông qua phương án tác chiến, thành lập Bộ Chỉ huy chiến dịch, Đảng ủy mặt trận do Đại tướng Võ Nguyên Giáp làm Tư lệnh kiêm Bí thư Đảng ủy chiến dịch.</p>
            <p>Ngày 25/01/1954, các đơn vị bộ đội của ta đã ở vị trí tập kết và sẵn sàng nổ súng theo phương châm tác chiến “đánh nhanh, thắng nhanh”. Trong quá trình đó, do đã nhận thấy địch đã tăng cường lực lượng phòng ngự vững chắc, Bộ Chỉ huy và Đảng ủy chiến dịch đã đưa ra quyết định đúng đắn: giữ vững quyết tâm tiêu diệt tập đoàn cứ điểm Điện Biên Phủ, thay đổi phương châm tác chiến từ “đánh nhanh, thắng nhanh” sang “đánh chắc, tiến chắc”.</p>
            <p><strong>Chiến dịch Điện Biên Phủ đã diễn ra 3 đợt:</strong></p>
            <ul>
            <li><strong>Đợt 1:</strong> Từ ngày 13 đến 17/3/1954: Quân ta đã dũng cảm, mưu trí tiêu diệt gọn cụm cứ điểm Him Lam và Độc Lập, bức hàng cứ điểm Bản Kéo, phá vỡ cửa ngõ phía Bắc của tập đoàn cứ điểm Điện Biên Phủ; tiêu diệt và bắt sống trên 2.000 quân địch, xóa sổ 01 trung đoàn, phá hủy 25 máy bay, uy hiếp sân bay Mường Thanh; tư lệnh pháo binh Pháp ở Điện Biên Phủ – đại tá Pirốt bất lực trước pháo binh của ta nên đã dùng lựu đạn để tự sát.</li>
            <li><strong>Đợt 2:</strong> Từ ngày 30/3 đến ngày 30/4/1954: Quân ta đồng loạt tiến công các cứ điểm phía Đông phân khu trung tâm thắt chặt vòng vây, chia cắt và liên tục tiến công, kiểm soát sân bay Mường Thanh, hạn chế tiếp viện của địch cho các tập đoàn cứ điểm. Để muốn kéo dài thời gian nên quân địch hết sức ngoan cố. Tướng Nava hy vọng đến mùa mưa ta phải cởi vòng vây. Đây là đợt tấn công dai dẳng, quyết liệt nhất, dài ngày nhất, cam go nhất, ta và địch giành giật nhau từng đoạn giao thông hào và từng tấc đất. Đặc biệt, tại đồi C1 quân ta và quân Pháp đã giằng co nhau tới 20 ngày, tại đồi A1 đã giằng co tới 30 ngày. Sau đợt tấn công thứ 2, khu trung tâm Điện Biên Phủ đã nằm trong tầm bắn của các nình súng bên quân ta, quân địch rơi vào tình trạng bị động và tinh thần chiến đấu giảm xuống rõ dệt.</li>
            <li><strong>Đợt 3:</strong> Từ ngày 01/5 đến ngày 07/5/1954: Quân ta đánh chiếm các cứ điểm phía Đông và mở đợt tổng tiến công tiêu diệt toàn bộ tập đoàn cứ điểm Điện Biên Phủ. Đêm ngày 06/5/1954, tại đồi A1 trận chiến đấu giữa quân ta và quân địch diễn ra rất quyết liệt, quân ta ào ạt xông lên đã tiêu diệt các lô cốt và dùng thuốc nổ để phá các hầm ngầm. Tên chỉ huy đồi A1 và khoảng 400 tên địch còn sống sót đã phải xin đầu hàng. Vào lúc 17 giờ 30 phút ngày 07/5/1954, quân ta đã chiếm sở chỉ huy của địch, tướng Đờ Cát cùng toàn bộ Bộ Tham mưu và binh lính tập đoàn cứ điểm Điện Biên Phủ phải xin đầu hàng. Lá cờ “quyết chiến, quyết thắng” của quân ta tung bay trên nóc hầm chỉ huy của quân địch. Và ngay trong đêm đó, quân ta tiếp tục tiến công phân khu Nam, đánh cho địch tháo chạy về Thượng Lào, đến 24 giờ thì tất cả toàn bộ quân của địch đã bị quân ta bắt.</li>
            </ul>
